    with open(r'c:\Users\tesla\Videos\Nouvelle aventure\pret\client\messages\fr.json', 'r', encoding='utf-8') as f:
        fr = json.load(f)
    
    reviews = fr['Reviews']['Items']
    ro_reviews = {}
    
    for r_id, r_data in reviews.items():
        original_region = r_data['region']
        original_comment = r_data['comment']
        
        ro_region = regions_map.get(original_region, original_region)
        
        # Check if direct comment exists
        ro_comment = comments_map.get(original_comment, original_comment)
            
        ro_reviews[r_id] = {
            "region": ro_region,
            "comment": ro_comment
        }
        
    with open('ro_reviews_output.json', 'w', encoding='utf-8') as f:
        json.dump(ro_reviews, f, indent=2, ensure_ascii=False)

